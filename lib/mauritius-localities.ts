// Case-insensitive lookup with fuzzy matching
export function getLocalityCoords(locality: string): { lat: number; lng: number } | null {
  if (!locality) return null
  
  // Direct match
  if (MAURITIUS_LOCALITIES[locality]) return MAURITIUS_LOCALITIES[locality]
  
  // Case-insensitive match
  const lower = locality.toLowerCase().trim()
  for (const [key, coords] of Object.entries(MAURITIUS_LOCALITIES)) {
    if (key.toLowerCase() === lower) return coords
  }
  
  // Partial match (e.g., "Grand Bay Centre" -> "Grand Bay")
  for (const [key, coords] of Object.entries(MAURITIUS_LOCALITIES)) {
    if (lower.includes(key.toLowerCase()) || key.toLowerCase().includes(lower)) return coords
  }
  
  return null
}
