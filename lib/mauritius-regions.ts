/**
 * Get coordinates for a region name
 * @param regionName - The name of the region (case-insensitive)
 * @returns The coordinates or null if not found
 */
export function getRegionCoords(regionName: string): { lat: number; lng: number } | null {
  if (!regionName) return null
  
  // Try exact match first
  if (MAURITIUS_REGIONS[regionName]) {
    return MAURITIUS_REGIONS[regionName]
  }
  
  // Try case-insensitive match
  const lowerName = regionName.toLowerCase()
  for (const [key, coords] of Object.entries(MAURITIUS_REGIONS)) {
    if (key.toLowerCase() === lowerName) {
      return coords
    }
  }
  
  // Try partial match
  for (const [key, coords] of Object.entries(MAURITIUS_REGIONS)) {
    if (key.toLowerCase().includes(lowerName) || lowerName.includes(key.toLowerCase())) {
      return coords
    }
  }
  
  return null
}

