// Helper function to get region coordinates
// ══════════════════════════════════════════════════════════════════════════════
export function getRegionCoords(region: string): { lat: number; lng: number } | null {
  // Try exact match first
  if (MAURITIUS_REGIONS[region]) return MAURITIUS_REGIONS[region]
  
  // Try uppercase
  const upper = region.toUpperCase()
  if (MAURITIUS_REGIONS[upper]) return MAURITIUS_REGIONS[upper]
  
  // Try title case
  const title = region.split(' ').map(w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase()).join(' ')
  if (MAURITIUS_REGIONS[title]) return MAURITIUS_REGIONS[title]
  
  // Try lowercase match
  const lower = region.toLowerCase()
  for (const key of Object.keys(MAURITIUS_REGIONS)) {
    if (key.toLowerCase() === lower) return MAURITIUS_REGIONS[key]
  }
  
  // Default to center of Mauritius if not found
  return { lat: -20.2, lng: 57.5 }
}

